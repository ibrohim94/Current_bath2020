package Day2_04122020;

public class for_loop {

    public static void main(String[] args) {

        String[] states = new String[5];
        states [0] = "New York";
        states [1] = "California";
        states [2] = "Ohio";
        states [3] = "Arizona";
        states [4] = "Nevada";

        //defining the loop
        //print all the states from 0 to 3 in sequence
        //i ++ each time is adding +1 to 1
        //i represents your initial/starting point of your index
        for (int i = 0; i <= 4; i++) {

            System.out.println("My states is " + states[i]);

        }//end of for loop



    }// end of main method


    }//end of java class
